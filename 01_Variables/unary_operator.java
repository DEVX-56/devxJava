public class unary_operator {
    public static void main(String[] args) {

        int a = 10;
        int b = a++;   //first print then increment
        int c = ++a;  //first increment then print
        System.out.println(b);
        System.out.println(c);
        
    }
    
}


/*
ad we know a+1 = a++
and a-1 = a-- these operator called unary operator
 */
