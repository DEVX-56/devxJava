//for loop in java
public class loop_for {
    public static void main(String[] args) {
        //incrementing
        for(int i=0; i<=10; i++){
            System.out.println(i);
        }
        System.out.println("\n\n***************************************************************************\n\n");
        //Decrementing
        for(int i=5; i>0; i--){
            System.out.println(i);
        }
    }
    
}
